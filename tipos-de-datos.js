// tipos de datos en variables

// undefined
var x;
var x = undefined;

// number
var n = 123;   // entero
var t = 35.8;  // decimal

// string (cadena de texto o cadena de caracteres)
// entre comillas es un string sin importar el contenido
var a = ''; // texto vacio, de longitud 0
var b = 'hola mundo'; // de longitud 10
var c = '123';
var d = "comillas dobles"; // es igual el tipo de comilla

// boolean - solo admite dos valores
var verdad  = true;  // un 1 puede ser interpretado como true 
var mentira = false; // un 0 puede ser interpreado como false
